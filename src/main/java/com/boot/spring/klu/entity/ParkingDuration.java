package com.boot.spring.klu.entity;

public class ParkingDuration {

	String id;
	String open_id;
	String parking_b;
	String parking_o;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOpen_id() {
		return open_id;
	}
	public void setOpen_id(String open_id) {
		this.open_id = open_id;
	}
	public String getParking_b() {
		return parking_b;
	}
	public void setParking_b(String parking_b) {
		this.parking_b = parking_b;
	}
	public String getParking_o() {
		return parking_o;
	}
	public void setParking_o(String parking_o) {
		this.parking_o = parking_o;
	}
	
		
}
